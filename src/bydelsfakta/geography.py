def bydel_by_id(bydel_id):
    """Look up a district by its 2-digit ID."""
    return BYDELER.get(str(bydel_id))


def bydel_by_name(name):
    """Look up a district by name (case-insensitive).

    Tries both the raw name and with "Bydel " prefix, matching
    the fallback logic in bydeler.jslt.
    """
    lower = name.lower().strip()
    bydel_id = _BYDELER_BY_NAME.get(lower)

    if bydel_id is None:
        bydel_id = _BYDELER_BY_NAME.get("bydel " + lower)

    return None if bydel_id is None else BYDELER.get(bydel_id)


def delbydel_by_id(numeric_id):
    """Look up a sub-district by its numeric ID (e.g., 101 -> '0101')."""
    return DELBYDELER.get(str(int(numeric_id)).zfill(4))


def delbydel_by_name(name):
    """Look up a sub-district by name (case-insensitive)."""
    delbydel_id = _DELBYDELER_BY_NAME.get(name.lower().strip())
    return None if delbydel_id is None else DELBYDELER.get(delbydel_id)


def delbydel_by_old_id(old_id):
    """Look up a sub-district by old 3-digit ID (e.g., '011' -> '0101')."""
    padded = old_id if isinstance(old_id, str) else str(int(old_id)).zfill(3)
    new_id = _DELBYDEL_BY_OLD_ID.get(padded)
    return None if new_id is None else DELBYDELER.get(new_id)


def get_district_id(name):
    """Return the 2-digit district ID for a district name."""
    if not name.startswith("Bydel ") and name not in [
        "Marka",
        "Sentrum",
        "Oslo i alt",
        "Uten registrert adresse",
    ]:
        name = f"Bydel {name}"
    return _BYDELER_BY_NAME[name.lower().strip()]
